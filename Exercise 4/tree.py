class Node:

    def __init__(self, label, data, parent):
        self.__label = label
        self.__data = data
        self.__parent = parent
        self.__children = []

    def set_children(children):
        self.children = children

    def get_children():
        return self.children

    def get_parent():
        return self.parent

    def get_data():
        return self.data

    def set_data(data):
        self.data = data
